import React from "react";
function DayButtons({ daysid }) {
    return (
        <ul>
            {daysid.map((day) => {
                if (day % 4 === 0) {
                    return <li>Day 4</li>;
                } else {
                    return <li>Day {day % 4}</li>;
                }
            })}
        </ul>
    );
}

export default DayButtons;
